/**
 * 將字體資料庫轉換為系統提示詞格式
 */
export function buildFontDatabasePrompt(): string {
  return dynaFonts
    .map(
      (font) =>
        `- ${font.name}（${font.style}）：${font.description}\n  推薦場景：${font.useCases}`,
    )
    .join("\n");
}
